package com.iiclab.dto;

public class GraduatedResearcherDto {
    private String name;
    private String student_number;
    private String grade;
    private String employment_place;

    // Getters and Setters
    public String getName() { return name; }
    public void setName(String name) { this.name = name; }
    public String getStudent_number() { return student_number; }
    public void setStudent_number(String student_number) { this.student_number = student_number; }
    public String getGrade() { return grade; }
    public void setGrade(String grade) { this.grade = grade; }
    public String getEmployment_place() { return employment_place; }
    public void setEmployment_place(String employment_place) { this.employment_place = employment_place; }
}
