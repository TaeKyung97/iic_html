package com.iiclab.dto;

public class StatisticsDto {
    private int thesisCount;
    private int researcherCount;
    private int activitiesCount;

    // Getter Setter
    public int getThesisCount() { return thesisCount; }
    public void setThesisCount(int thesisCount) { this.thesisCount = thesisCount; }
    public int getResearcherCount() { return researcherCount; }
    public void setResearcherCount(int researcherCount) { this.researcherCount = researcherCount; }
    public int getActivitiesCount() { return activitiesCount; }
    public void setActivitiesCount(int activitiesCount) { this.activitiesCount = activitiesCount; }
}

